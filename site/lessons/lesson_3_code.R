# ====== –≠–¢–ê–ü 1: –ü–†–û–°–¢–û–ô –¶–ò–ö–õ FOR (–ë–ê–ó–û–í–´–ô –ü–†–ò–ú–ï–†) ======
# üìå –í —ç—Ç–æ–º –±–ª–æ–∫–µ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è:
# - `for (i in 1:5) {...}` ‚Äî –≤—ã–ø–æ–ª–Ω—è–µ–º –∫–æ–¥ 5 —Ä–∞–∑, –ø–µ—á–∞—Ç–∞—è –Ω–æ–º–µ—Ä –∏—Ç–µ—Ä–∞—Ü–∏–∏.

for (i in 1:5) {
