# ====== –≠–¢–ê–ü 2: –ü–†–û–°–¢–û–ô –¶–ò–ö–õ FOR (–†–ê–°–®–ò–†–ï–ù–ù–´–ô –ü–†–ò–ú–ï–†)======
# üìå –í —ç—Ç–æ–º –±–ª–æ–∫–µ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è:
# - `for (i in 1:10) {...}` ‚Äî —Ü–∏–∫–ª, –≤—ã–ø–æ–ª–Ω—è—é—â–∏–π –∫–æ–¥ 10 —Ä–∞–∑.
# - `print()` ‚Äî –≤—ã–≤–æ–¥–∏—Ç —Ç–µ–∫—Å—Ç –≤ –∫–æ–Ω—Å–æ–ª—å.
# - `paste0()` ‚Äî –æ–±—ä–µ–¥–∏–Ω—è–µ—Ç —Å—Ç—Ä–æ–∫–∏ –±–µ–∑ –ø—Ä–æ–±–µ–ª–æ–≤.
# - `Sys.sleep(1)` ‚Äî –¥–µ–ª–∞–µ—Ç –ø–∞—É–∑—É –≤ 1 —Å–µ–∫—É–Ω–¥—É.

for (i in 1:10) {
  print(paste0("–ù–∞—á–∏–Ω–∞–µ—Ç –≤—ã–ø–æ–ª–Ω—è—Ç—å—Å—è —Ü–∏–∫–ª ", i, " ..."))
  Sys.sleep(1)
  print(paste0("i –≤ —ç—Ç–æ–º —Ü–∏–∫–ª–µ —Ä–∞–≤–Ω—è–µ—Ç—Å—è: ", i))
  Sys.sleep(1)
  print(paste0("...–∑–∞–∫–∞–Ω—á–∏–≤–∞–µ—Ç –≤—ã–ø–æ–ª–Ω—è—Ç—å—Å—è —Ü–∏–∫–ª ", i, "."))
  Sys.sleep(1)
}

# ====== –≠–¢–ê–ü 3: –¶–ò–ö–õ FOR –î–õ–Ø –°–ü–ò–°–ö–ê –î–ù–ï–ô –ù–ï–î–ï–õ–ò ======
# üìå –í —ç—Ç–æ–º –±–ª–æ–∫–µ –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è:
# - `c()` ‚Äî —Å–æ–∑–¥–∞—ë—Ç –≤–µ–∫—Ç–æ—Ä —Å –Ω–∞–∑–≤–∞–Ω–∏—è–º–∏ –¥–Ω–µ–π –Ω–µ–¥–µ–ª–∏.
# - `for (day in weekdays) {...}` ‚Äî –ø–µ—Ä–µ–±–∏—Ä–∞–µ–º –∫–∞–∂–¥—ã–π –¥–µ–Ω—å –Ω–µ–¥–µ–ª–∏.

weekdays <- c("–ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫", "–í—Ç–æ—Ä–Ω–∏–∫", "–°—Ä–µ–¥–∞", "–ß–µ—Ç–≤–µ—Ä–≥", "–ü—è—Ç–Ω–∏—Ü–∞", "–°—É–±–±–æ—Ç–∞", "–í–æ—Å–∫—Ä–µ—Å–µ–Ω—å–µ")

for (day in weekdays) {
  print(paste0("–ù–∞—á–∏–Ω–∞–µ—Ç—Å—è ", day, ", —É—Ä–∞!"))
  Sys.sleep(1)
  print(paste0("–ó–∞–∫–∞–Ω—á–∏–≤–∞–µ—Ç—Å—è ", day, ". –î–µ–Ω—å –±—ã–ª —Ç—Ä—É–¥–µ–Ω, –Ω–æ –ø—Ä–æ–¥—É–∫—Ç–∏–≤–µ–Ω."))
  Sys.sleep(1)
}

# ====== –≠–¢–ê–ü 4: –í–´–ë–û–†–ö–ê –¢–û–õ–¨–ö–û –†–ê–ë–û–ß–ò–• –î–ù–ï–ô ======
# üìå –ó–¥–µ—Å—å –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è:
# - `weekdays[1:5]` ‚Äî –≤—ã–±–∏—Ä–∞–µ–º —Ç–æ–ª—å–∫–æ –ø–µ—Ä–≤—ã–µ 5 —ç–ª–µ–º–µ–Ω—Ç–æ–≤ (—Ä–∞–±–æ—á–∏–µ –¥–Ω–∏).

for (day in weekdays[1:5]) {
  print(paste0("–ù–∞—á–∏–Ω–∞–µ—Ç—Å—è ", day, ", —Ä–∞–±–æ—Ç–∞ –∫–∏–ø–∏—Ç!"))
  Sys.sleep(1)
  print(paste0("–ó–∞–∫–∞–Ω—á–∏–≤–∞–µ—Ç—Å—è ", day, ". –ü–æ—Ä–∞ –æ—Ç–¥—ã—Ö–∞—Ç—å!"))
  Sys.sleep(1)
}

# ====== –≠–¢–ê–ü 5: –£–°–¢–ê–ù–û–í–ö–ê –ò –ü–û–î–ö–õ–Æ–ß–ï–ù–ò–ï –ü–ê–ö–ï–¢–û–í ======
# üìå –ó–¥–µ—Å—å –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è:
# - `install.packages("dplyr")` ‚Äî —É—Å—Ç–∞–Ω–æ–≤–∫–∞ –ø–∞–∫–µ—Ç–∞ (–∑–∞–∫–æ–º–º–µ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–æ).
# - `library(dplyr)` ‚Äî –ø–æ–¥–∫–ª—é—á–µ–Ω–∏–µ –ø–∞–∫–µ—Ç–∞ dplyr.

# install.packages("dplyr")
library(dplyr)

# ====== –≠–¢–ê–ü 6: –ó–ê–ì–†–£–ó–ö–ê –î–ê–ù–ù–´–• –ò –ò–ó–£–ß–ï–ù–ò–ï –°–¢–†–£–ö–¢–£–†–´ ======
# üìå –ó–¥–µ—Å—å –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è:
# - `colnames()` ‚Äî –≤—ã–≤–æ–¥–∏—Ç –Ω–∞–∑–≤–∞–Ω–∏—è —Å—Ç–æ–ª–±—Ü–æ–≤ —Ç–∞–±–ª–∏—Ü—ã.
# - `$` ‚Äî –æ–±—Ä–∞—â–µ–Ω–∏–µ –∫ –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–º—É —Å—Ç–æ–ª–±—Ü—É.

starwars <- starwars
colnames(starwars)

starwars$birth_year
starwars$sex
starwars$gender

# ====== –≠–¢–ê–ü 7: –í–´–í–û–î –ò–ú–ï–ù –ü–ï–†–°–û–ù–ê–ñ–ï–ô ======
# üìå –ó–¥–µ—Å—å –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è:
# - `length(starwars$name)` ‚Äî –ø–æ–ª—É—á–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –≤ –∫–æ–ª–æ–Ω–∫–µ `name`.

for (i in 1:length(starwars$name)) {
  print(paste0("–ò–º—è –ø–µ—Ä—Å–æ–Ω–∞–∂–∞: ", starwars$name[i]))
  Sys.sleep(0.3)
}

# ====== –≠–¢–ê–ü 8: –í–´–í–û–î –ò–ú–ï–ù–ò –ò –ú–ï–°–¢–ê –†–û–ñ–î–ï–ù–ò–Ø ======
# üìå –ó–¥–µ—Å—å –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è:
# - `paste0()` ‚Äî –æ–±—ä–µ–¥–∏–Ω—è–µ—Ç —Ç–µ–∫—Å—Ç –∏ –∑–Ω–∞—á–µ–Ω–∏—è –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö.

for (i in 1:length(starwars$name)) {
  print(paste0(
    "–ü–µ—Ä—Å–æ–Ω–∞–∂ ", starwars$name[i], 
    " —Ä–æ–¥–∏–ª—Å—è –Ω–∞ –ø–ª–∞–Ω–µ—Ç–µ ", starwars$homeworld[i], "."
  ))
  Sys.sleep(0.3)
}

# ====== –≠–¢–ê–ü 9: –†–ê–°–ß–Å–¢ –ò–ù–î–ï–ö–°–ê –ú–ê–°–°–´ –¢–ï–õ–ê (–ò–ú–¢) ======
# üìå –ó–¥–µ—Å—å –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è:
# - `round()` ‚Äî –æ–∫—Ä—É–≥–ª—è–µ—Ç —á–∏—Å–ª–æ –¥–æ —É–∫–∞–∑–∞–Ω–Ω–æ–≥–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –∑–Ω–∞–∫–æ–≤.
# - –§–æ—Ä–º—É–ª–∞: –ò–ú–¢ = –º–∞—Å—Å–∞ (–∫–≥) / (—Ä–æ—Å—Ç (–º)^2).

for (i in 1:length(starwars$name)) {
  print(paste0(
    "–ü–µ—Ä—Å–æ–Ω–∞–∂ ", starwars$name[i], 
    " —Ä–æ–¥–∏–ª—Å—è –Ω–∞ –ø–ª–∞–Ω–µ—Ç–µ ", starwars$homeworld[i], ". ",
    "–ï–≥–æ/–µ—ë –ò–ú–¢ —Ä–∞–≤–Ω—è–µ—Ç—Å—è: ", round(starwars$mass[i] / ((starwars$height[i] * 0.01)^2), 1)
  ))
  Sys.sleep(0.3)
}

# ====== –≠–¢–ê–ü 10: –¶–ò–ö–õ WHILE –î–õ–Ø –†–ê–°–ß–Å–¢–ê –ò–ú–¢ ======
# üìå –ó–¥–µ—Å—å –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è:
# - `while (—É—Å–ª–æ–≤–∏–µ) {...}` ‚Äî —Ü–∏–∫–ª –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è, –ø–æ–∫–∞ –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è —É—Å–ª–æ–≤–∏–µ.

i <- 1
while (i <= 20) {
  print(paste0(
    "–ü–µ—Ä—Å–æ–Ω–∞–∂ ", starwars$name[i], 
    " —Ä–æ–¥–∏–ª—Å—è –Ω–∞ –ø–ª–∞–Ω–µ—Ç–µ ", starwars$homeworld[i], ". ",
    "–ï–≥–æ/–µ—ë –ò–ú–¢ —Ä–∞–≤–Ω—è–µ—Ç—Å—è: ", round(starwars$mass[i] / ((starwars$height[i] * 0.01)^2), 1)
  ))
  Sys.sleep(0.05)
  print(i)
  i <- i + 1
}

# ====== –≠–¢–ê–ü 11: –ü–†–û–ü–£–°–ö –ù–ï–ß–ï–õ–û–í–ï–ß–ï–°–ö–ò–• –ü–ï–†–°–û–ù–ê–ñ–ï–ô ======
# üìå –ó–¥–µ—Å—å –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è:
# - `if (—É—Å–ª–æ–≤–∏–µ) {...}` ‚Äî –ø—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ –ø–µ—Ä—Å–æ–Ω–∞–∂ —á–µ–ª–æ–≤–µ–∫–æ–º, –ø–æ–Ω–∏–º–∞—è, —á—Ç–æ –µ—Å–ª–∏ —É –Ω–µ–≥–æ —É–∫–∞–∑–∞–Ω –ø–æ–ª, —Ç–æ –æ–Ω –Ω–µ —Ä–æ–±–æ—Ç.

for (i in 1:length(starwars$name)) {
  if (starwars$sex[i] %in% c("none", NA)) {
    print("–≠—Ç–æ –Ω–µ —á–µ–ª–æ–≤–µ–∫, –ø—Ä–æ–ø—É—Å–∫–∞–µ–º —Ä–∞—Å—á—ë—Ç –ò–ú–¢...")
  } else {
    print(paste0(
      "–ü–µ—Ä—Å–æ–Ω–∞–∂ ", starwars$name[i], 
      " —Ä–æ–¥–∏–ª—Å—è –Ω–∞ –ø–ª–∞–Ω–µ—Ç–µ ", starwars$homeworld[i], ". ",
      "–ï–≥–æ/–µ—ë –ò–ú–¢ —Ä–∞–≤–Ω—è–µ—Ç—Å—è: ", round(starwars$mass[i] / ((starwars$height[i] * 0.01)^2), 1)
    ))
  }
  Sys.sleep(0.5)
}

# ====== –≠–¢–ê–ü 12: –£–°–õ–û–í–ù–´–ï –í–´–†–ê–ñ–ï–ù–ò–Ø –° IFELSE() ======
# üìå –ó–¥–µ—Å—å –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è:
# - `ifelse(—É—Å–ª–æ–≤–∏–µ, "–µ—Å–ª–∏ –≤–µ—Ä–Ω–æ", "–µ—Å–ª–∏ –ª–æ–∂–Ω–æ")` ‚Äî –ø—Ä–æ–≤–µ—Ä—è–µ–º —É—Å–ª–æ–≤–∏–µ.

ifelse(3 > 2, "–≤–µ—Ä–Ω–æ", "–Ω–µ–≤–µ—Ä–Ω–æ")
ifelse(100 > 2, "–≤–µ—Ä–Ω–æ", "–Ω–µ–≤–µ—Ä–Ω–æ")
ifelse(2 > 2, "–≤–µ—Ä–Ω–æ", "–Ω–µ–≤–µ—Ä–Ω–æ")
ifelse(1 > 2, "–≤–µ—Ä–Ω–æ", "–Ω–µ–≤–µ—Ä–Ω–æ")
ifelse(-4 > 2, "–≤–µ—Ä–Ω–æ", "–Ω–µ–≤–µ—Ä–Ω–æ")

# ====== –≠–¢–ê–ü 13: –ü–†–û–ì–†–ê–ú–ú–ê –î–õ–Ø –û–ü–†–ï–î–ï–õ–ï–ù–ò–Ø –õ–£–ß–®–ï–ì–û –î–ù–Ø ======
# üìå –ó–¥–µ—Å—å –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è:
# - `if`, `else if`, `else` ‚Äî —É—Å–ª–æ–≤–Ω—ã–µ –æ–ø–µ—Ä–∞—Ç–æ—Ä—ã.

for (day in weekdays) {
  if (day == "–ß–µ—Ç–≤–µ—Ä–≥") {
    print(paste0("–£—Ä–∞, —Å–µ–≥–æ–¥–Ω—è ", day, ", –∞ —ç—Ç–æ –æ–∑–Ω–∞—á–∞–µ—Ç, —á—Ç–æ –º—ã —É–∑–Ω–∞—ë–º –Ω–æ–≤–æ–µ –Ω–∞ –∑–∞–Ω—è—Ç–∏–∏ –ø–æ —è–∑—ã–∫—É R!"))
    Sys.sleep(4)
    
  } else if (day %in% c("–°—É–±–±–æ—Ç–∞", "–í–æ—Å–∫—Ä–µ—Å–µ–Ω—å–µ")) {
    print(paste0("–•–æ-—Ö–æ-—Ö–æ, —Å–µ–≥–æ–¥–Ω—è ", day, ", –≤—ã—Ö–æ–¥–Ω–æ–π –¥–µ–Ω—å, –∏ –º—ã –º–æ–∂–µ–º –ø—Ä–æ—á–∏—Ç–∞—Ç—å —á—Ç–æ-—Ç–æ –Ω–æ–≤–æ–µ –ø—Ä–æ R!"))
    Sys.sleep(1)
    
  } else {
    print(paste0("–°–µ–≥–æ–¥–Ω—è ", day, " ‚Äì –æ–±—ã—á–Ω—ã–π —Ä–∞–±–æ—á–∏–π –¥–µ–Ω—å, –≤–µ–¥—å —ç—Ç–æ –Ω–µ —á–µ—Ç–≤–µ—Ä–≥..."))
    Sys.sleep(1)
  }
}
